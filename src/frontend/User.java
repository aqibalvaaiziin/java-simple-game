    public void showDataUser(){
        String[] columnData = {"ID User", "Username", "Hero", "Packet Potion"};
        ArrayList<Users> listUser = new Users().getAll(); 
        Object rowData[] = new Object[4];
        
        userTable.setModel(new DefaultTableModel(new Object[][] {}, columnData));
        
        for(Users user : listUser){
            rowData[0] = user.getIdUser();
            rowData[1] = user.getUsername();
            rowData[2] = user.getHero().getHeroName();
            rowData[3] = user.getBag().getBagName();
            ((DefaultTableModel)userTable.getModel()).addRow(rowData);
        }
    }
    
    public void showDataHero(){
        String[] columnData = {"ID","Hero","Role","HP","Attack","Speed","Defence"};
        ArrayList<Melee> listHero = new Melee().getAll();
        Object rowData[] = new Object[7];
        
        heroTable.setModel(new DefaultTableModel(new Object[][] {}, columnData));
        
        for(Melee melee : listHero){
            rowData[0] = melee.getIdHero();
            rowData[1] = melee.getHeroName();
            rowData[2] = melee.getRole();
            rowData[3] = melee.getHp();
            rowData[4] = melee.getAtk();
            rowData[5] = melee.getSpd();
            rowData[6] = melee.getDfc();
            ((DefaultTableModel)heroTable.getModel()).addRow(rowData);
        }
    }

    public void showDataBag(){
        String[] columnData = {"ID","Package Name"};
        ArrayList<Bag> listBag = new Bag().getAllBagName();
        Object rowData[] = new Object[2];
        
        bagTable.setModel(new DefaultTableModel(new Object[][] {}, columnData));
        
        for(Bag bag : listBag){
            rowData[0] = bag.getIdBag();
            rowData[1] = bag.getBagName();
            ((DefaultTableModel)bagTable.getModel()).addRow(rowData);
        }
    }
    
