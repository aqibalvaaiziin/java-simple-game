    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Users user = new Users();
        user.setIdUser(Integer.parseInt(txtId.getText()));
        user.setUsername(txtUsername.getText());
        user.getHero().setIdHero(Integer.parseInt((String)comboHero.getSelectedItem()));
        user.getBag().setIdBag(Integer.parseInt((String) comboBag.getSelectedItem()));
        user.save();
        txtId.setText(Integer.toString(user.getIdUser()));
        showDataUser();
    }//GEN-LAST:event_btnSaveActionPerformed

