    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();
        int row = userTable.getSelectedRow();

        Users user = (Users) new Users().getDataById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        user.delete();
        emptyForm();
        showDataUser();
    }//GEN-LAST:event_deletebtnActionPerformed

    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();
        int row = userTable.getSelectedRow();
        Users user = new Users();
        
        user= (Users) user.getDataById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        txtId.setText(model.getValueAt(row, 0).toString());
        txtUsername.setText(model.getValueAt(row, 1).toString());
        comboHero.getModel().setSelectedItem(user.getHero());
        comboBag.getModel().setSelectedItem(user.getBag());
    }//GEN-LAST:event_userTableMouseClicked

