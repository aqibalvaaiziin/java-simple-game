    private void heroTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_heroTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) heroTable.getModel();
        int row = heroTable.getSelectedRow();
        Melee melee = new Melee();
        
        melee= (Melee) melee.getDataById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        txtIdHero.setText(model.getValueAt(row, 0).toString());
        txtHeroName.setText(model.getValueAt(row, 1).toString());
        comboRole.getModel().setSelectedItem(melee.getRole());
        txtHp.setText(model.getValueAt(row, 3).toString());
        txtAttack.setText(model.getValueAt(row, 4).toString());
        txtSpeed.setText(model.getValueAt(row, 5).toString());
        txtDefends.setText(model.getValueAt(row, 6).toString());
    }//GEN-LAST:event_heroTableMouseClicked

