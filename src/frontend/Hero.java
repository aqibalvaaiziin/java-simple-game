    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Melee melee = new Melee();
        melee.setIdHero(Integer.parseInt(txtIdHero.getText()));
        melee.setHeroName(txtHeroName.getText());
        melee.setRole((String) comboRole.getSelectedItem());
        melee.setHp(Integer.parseInt(txtHp.getText()));
        melee.setAtk(Integer.parseInt(txtAttack.getText()));
        melee.setSpd(Integer.parseInt(txtSpeed.getText()));
        melee.setDfc(Integer.parseInt(txtDefends.getText()));
        melee.save();
        txtIdHero.setText(Integer.toString(melee.getIdHero()));
        showData();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) heroTable.getModel();
        int row = heroTable.getSelectedRow();

        Melee melee = (Melee) new Melee().getDataById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        melee.delete();
        emptyForm();
        showData();
    }//GEN-LAST:event_btnDeleteActionPerformed

